class APIError(Exception):
    def __init__(
        self,
        *,
        code: int | ErrorCode,
        message: str,
        status_code: int = 400,
        data: Any = None,
    ) -> None:
        self.code = int(code)
        self.message = message
        self.status_code = status_code
        self.data = data
        super().__init__(message)
